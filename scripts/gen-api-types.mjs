#!/usr/bin/env node
/**
 * scripts/gen-api-types.mjs
 *
 * 从 fab-3d-world-api 拉 openapi.yaml + 生成 src/api/types.ts。
 *
 * 用法:
 *   npm run gen:api                                          # 从 GitHub main 分支拉
 *   node scripts/gen-api-types.mjs --local <path/to/openapi.yaml>   # 本地 spec
 *
 * 失败时（spec 不存在 / 网络问题）打 warn + exit 0，不阻断 build。
 * 想强制失败：传 --strict。
 *
 * **web + pc 各一份同源副本**（与 axios.js / viewerSoul.js 等同规则），改动须同步另一端。
 */

import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')

const OPENAPI_URL =
  process.env.FAB_OPENAPI_URL ||
  'https://raw.githubusercontent.com/LionelHao/fab-3d-world-api/main/docs/openapi.yaml'
const OUT_FILE = path.join(ROOT, 'src/api/types.ts')

const args = process.argv.slice(2)
const localIdx = args.indexOf('--local')
const localPath = localIdx >= 0 ? args[localIdx + 1] : null
const strict = args.includes('--strict')

async function loadSpec() {
  if (localPath) {
    const fullPath = path.resolve(ROOT, localPath)
    console.log(`→ Loading local spec: ${fullPath}`)
    return await fs.readFile(fullPath, 'utf-8')
  }
  console.log(`→ Fetching spec: ${OPENAPI_URL}`)
  const res = await fetch(OPENAPI_URL)
  if (!res.ok) {
    throw new Error(
      `HTTP ${res.status} ${res.statusText} — has fab-3d-world-api committed docs/openapi.yaml to main yet?`,
    )
  }
  return await res.text()
}

async function main() {
  let spec
  try {
    spec = await loadSpec()
  } catch (e) {
    const msg = `⚠ Could not load openapi spec: ${e.message}`
    if (strict) {
      console.error(msg)
      process.exit(1)
    }
    console.warn(msg)
    console.warn('  Skipping type generation. Build will continue.')
    console.warn('  Run manually later: npm run gen:api')
    process.exit(0)
  }

  // openapi-typescript 7.x API
  const { default: openapiTS, astToString } = await import('openapi-typescript')
  const ast = await openapiTS(spec)

  const header = `// AUTO-GENERATED by scripts/gen-api-types.mjs — do not edit.
// Source: ${localPath || OPENAPI_URL}
// Generated: ${new Date().toISOString()}
//
// 用法见 docs/api-types.md

`
  const contents = header + astToString(ast)

  await fs.mkdir(path.dirname(OUT_FILE), { recursive: true })
  await fs.writeFile(OUT_FILE, contents)
  const bytes = Buffer.byteLength(contents)
  console.log(`✓ Generated: ${path.relative(ROOT, OUT_FILE)} (${bytes} bytes)`)
}

main().catch((e) => {
  console.error(`✗ ${e.message}`)
  process.exit(1)
})
